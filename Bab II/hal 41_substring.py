from __future__ import print_function

def main():
    a = "Python"

    print(a[0])
    print(a[-1])
    print(a[:2])   # pertama dari kanan
    print(a[2:])   # dikurangi 2 karakter pertama
    print(a[1:4])  # [ 1 sampai 3] == yth

if __name__ == "__main__":
    main()
